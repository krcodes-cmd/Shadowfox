@torch.no_grad()
def evaluate(model, loader, criterion_bin, criterion_multi, device, lambda_multi=0.5):
    """Evaluate model on a data loader."""
    model.eval()
    total_loss = 0.0
    all_preds_bin, all_preds_multi = [], []
    all_true_bin, all_true_multi = [], []

    for X_batch, y_bin, y_multi in loader:
        X_batch = X_batch.to(device)
        y_bin = y_bin.to(device)
        y_multi = y_multi.to(device)

        logits_bin, logits_multi = model(X_batch)

        loss_bin = criterion_bin(logits_bin, y_bin)
        loss_multi = criterion_multi(logits_multi, y_multi)
        loss = loss_bin + lambda_multi * loss_multi

        total_loss += loss.item() * X_batch.size(0)

        all_preds_bin.append(logits_bin.argmax(1).cpu())
        all_preds_multi.append(logits_multi.argmax(1).cpu())
        all_true_bin.append(y_bin.cpu())
        all_true_multi.append(y_multi.cpu())

    preds_bin = torch.cat(all_preds_bin).numpy()
    preds_multi = torch.cat(all_preds_multi).numpy()
    true_bin = torch.cat(all_true_bin).numpy()
    true_multi = torch.cat(all_true_multi).numpy()

    n = len(true_bin)
    return {
        "loss": total_loss / n,
        "acc_binary": accuracy_score(true_bin, preds_bin),
        "acc_multi": accuracy_score(true_multi, preds_multi),
        "preds_bin": preds_bin,
        "preds_multi": preds_multi,
        "true_bin": true_bin,
        "true_multi": true_multi,
    }


