    # -- Data Pipeline ---------------------------------------------------------
    train_loader, test_loader, info = prepare_data(CONFIG)
    print()

    # -- Model -----------------------------------------------------------------
    model = NexusRT(
        num_features=info["num_features"],
        num_classes_multi=info["num_subcat_classes"],
        hidden_dims=CONFIG["hidden_dims"],
        dropout=CONFIG["dropout_rate"],
    ).to(DEVICE)

    total_params = sum(p.numel() for p in model.parameters())
    trainable_params = sum(p.numel() for p in model.parameters() if p.requires_grad)
    print(f"[{MODEL_NAME}] Architecture: Dual-Head Residual MLP")
    print(f"[{MODEL_NAME}] Total Parameters    : {total_params:,}")
    print(f"[{MODEL_NAME}] Trainable Parameters: {trainable_params:,}")
    print(f"[{MODEL_NAME}] Hidden Dims: {CONFIG['hidden_dims']}")
    print()

    # -- Loss Functions --------------------------------------------------------
    # Binary: use higher gamma to focus on hard-to-classify profit examples
    criterion_bin = FocalLoss(
        weight=info["binary_class_weights"],
        gamma=1.5,
        label_smoothing=CONFIG["label_smoothing"],
    )
    criterion_multi = FocalLoss(
        weight=info["multi_class_weights"],
        gamma=2.0,
        label_smoothing=CONFIG["label_smoothing"],
    )

    # -- Optimizer + Scheduler -------------------------------------------------
    optimizer = optim.AdamW(
        model.parameters(),
        lr=CONFIG["learning_rate"],
        weight_decay=CONFIG["weight_decay"],
    )
    scheduler = optim.lr_scheduler.OneCycleLR(
        optimizer,
        max_lr=CONFIG["learning_rate"] * 3,
        epochs=CONFIG["max_epochs"],
        steps_per_epoch=len(train_loader),
        pct_start=0.3,
        anneal_strategy="cos",
    )

    # -- Training Loop ---------------------------------------------------------
    print("+------+------------+------------+------------+------------+------------+------------+")
    print("|Epoch | Train Loss | Train Bin% | Train Mul% |  Val Loss  |  Val Bin%  |  Val Mul%  |")
    print("+------+------------+------------+------------+------------+------------+------------+")

    best_val_combined = 0.0
    best_model_state = None
    best_val_acc_bin = 0.0
    best_val_acc_multi = 0.0
    history = []

    start_time = time.time()

    for epoch in range(1, CONFIG["max_epochs"] + 1):
        epoch_start = time.time()

        # -- Train -------------------------------------------------------------
        model.train()
        total_loss = 0.0
        total_correct_bin = 0
        total_correct_multi = 0
        total_samples = 0

        for X_batch, y_bin, y_multi in train_loader:
            X_batch = X_batch.to(DEVICE)
            y_bin = y_bin.to(DEVICE)
            y_multi = y_multi.to(DEVICE)

            optimizer.zero_grad()
            logits_bin, logits_multi = model(X_batch)

            loss_bin = criterion_bin(logits_bin, y_bin)
            loss_multi = criterion_multi(logits_multi, y_multi)
            # Give more weight to binary loss to boost binary accuracy
            loss = 1.5 * loss_bin + 0.5 * loss_multi

            loss.backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
            optimizer.step()
            scheduler.step()

            total_loss += loss.item() * X_batch.size(0)
            total_correct_bin += (logits_bin.argmax(1) == y_bin).sum().item()
            total_correct_multi += (logits_multi.argmax(1) == y_multi).sum().item()
            total_samples += X_batch.size(0)

        train_metrics = {
            "loss": total_loss / total_samples,
            "acc_binary": total_correct_bin / total_samples,
            "acc_multi": total_correct_multi / total_samples,
        }

        # -- Validate ----------------------------------------------------------
        val_metrics = evaluate(
            model, test_loader, criterion_bin, criterion_multi, DEVICE
        )

        epoch_time = time.time() - epoch_start

        # Track best combined accuracy
        combined = val_metrics["acc_binary"] + val_metrics["acc_multi"]
        if combined > best_val_combined:
            best_val_combined = combined
            best_model_state = {k: v.clone() for k, v in model.state_dict().items()}
        if val_metrics["acc_binary"] > best_val_acc_bin:
            best_val_acc_bin = val_metrics["acc_binary"]
        if val_metrics["acc_multi"] > best_val_acc_multi:
            best_val_acc_multi = val_metrics["acc_multi"]

        history.append({
            "epoch": epoch,
            "train_loss": train_metrics["loss"],
            "train_acc_bin": train_metrics["acc_binary"],
            "train_acc_multi": train_metrics["acc_multi"],
            "val_loss": val_metrics["loss"],
            "val_acc_bin": val_metrics["acc_binary"],
            "val_acc_multi": val_metrics["acc_multi"],
            "time": epoch_time,
        })

        bin_marker = " *" if val_metrics["acc_binary"] >= 0.80 else ""
        multi_marker = " *" if val_metrics["acc_multi"] >= 0.80 else ""

        print(
            f"|  {epoch:2d}  |  {train_metrics['loss']:.6f}  |  "
            f"{train_metrics['acc_binary']*100:6.2f}%   |  "
            f"{train_metrics['acc_multi']*100:6.2f}%   |  "
            f"{val_metrics['loss']:.6f}  |  "
            f"{val_metrics['acc_binary']*100:6.2f}%{bin_marker:<3s}|  "
            f"{val_metrics['acc_multi']*100:6.2f}%{multi_marker:<3s}|"
        )

    print("+------+------------+------------+------------+------------+------------+------------+")
    total_time = time.time() - start_time
    print(f"\n  * = Target achieved (>=80%)   Total training time: {total_time:.1f}s")

    # -- Load best model for final evaluation ----------------------------------
    if best_model_state is not None:
        model.load_state_dict(best_model_state)

    # =================================================================
